static void mkletter(SDL_Surface* sf, int c);

int gfx::mkfont() {
	if (gfx::font != NULL)  gfx::rmsprite(gfx::font);
	gfx::font = gfx::mksprite(128, 128);
	SDL_FillRect(gfx::font, NULL, gfx::drawc(255,0,255));
	SDL_Surface* letter = gfx::mksprite(8, 8);
	SDL_Rect r;
	for (int c=0; c<256; c++) {
		mkletter(letter, c);
		r.y = (c/16)*8,  r.x = (c%16)*8;
		SDL_BlitSurface(letter, NULL, gfx::font, &r);
	}
	gfx::rmsprite(letter);  // cleanup
	return 0;
}

SDL_Rect gfx::getc(int c) {
	return { short((c%16)*8), short((c/16)*8), 8, 8 };
}

int gfx::drawstr(SDL_Surface* dst, int x, int y, const std::string& str) {
	SDL_Rect sr, dr = { short(x), short(y), 8, 8 };
	for (int i=0; i<str.length(); i++) {
		sr = gfx::getc( str[i] );
		SDL_BlitSurface(gfx::font, &sr, dst, &dr);
		dr.x += 8;
	}
	return 0;
}



